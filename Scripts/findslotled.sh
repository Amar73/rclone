#!/usr/bin/env bash
set -euo pipefail

# ============================================================
# Скрипт: findslotled
#
# Назначение:
#   Определяет корзину и номер слота диска по имени устройства
#   и включает/выключает светодиод этого слота.
#
# Поддерживаемые действия:
#   on   - включить подсветку
#   off  - выключить подсветку
#   oof  - тоже выключить (защита от опечатки)
#   1    - включить
#   0    - выключить
#
# Примеры:
#   findslotled sdae on
#   findslotled /dev/sdac off
#   findslotled sdx oof
#
# Топология сервера:
#   target 0..23   -> enclosure 0:0:24:0 -> IN-WIN RS-424-07 -> slot = target + 1
#   target 24      -> зарезервирован (адрес enclosure 0:0:24:0)
#   target 25..36  -> enclosure 0:0:35:0 -> IN-WIN RS-212-07 -> slot = target - 24
#
# Важное замечание по именам элементов sysfs:
#   Ядро регистрирует директории слотов с trailing-пробелами,
#   например: "Bay Slot 03  " (два пробела в конце).
#   Переменная SLOT_DIR хранит путь строго как получен из glob —
#   с пробелами, иначе путь перестаёт существовать.
#   Для вывода используется SLOT_DIR_DISPLAY (пробелы обрезаны).
# ============================================================


usage() {
    cat <<'EOF'
Использование:
  findslotled <диск> <on|off>

Примеры:
  findslotled sdae on
  findslotled sdae off
  findslotled /dev/sdac on
  findslotled sdx oof

Пояснение:
  on   - включить подсветку слота
  off  - выключить подсветку слота
  oof  - тоже выключить (защита от опечатки)
EOF
    exit 1
}


# ------------------------------------------------------------
# Проверка: путь существует или является симлинком
# ------------------------------------------------------------
path_exists_or_link() {
    local p="$1"
    [[ -e "$p" || -L "$p" ]]
}


# ------------------------------------------------------------
# Обрезка trailing-пробелов — только для отображения.
#
# НЕ применять к путям, используемым для обращения к файловой
# системе: директории в sysfs зарегистрированы с пробелами
# и после обрезки перестают существовать.
# ------------------------------------------------------------
trim_trailing_spaces() {
    local s="$1"
    s="${s%"${s##*[![:space:]]}"}"
    printf '%s' "$s"
}


# ------------------------------------------------------------
# Извлечение номера слота из имени элемента sysfs.
#
# Примеры:
#   "Bay Slot 03  " -> 3
#   "Slot 11  "     -> 11
#   "slot07"        -> 7
#
# tail -n1 берёт последнее найденное число, что защищает
# от имён вида "Bay 2 Slot 03".
# ------------------------------------------------------------
extract_slot_number() {
    local name="$1"
    local num

    num="$(echo "$name" | grep -oE '[0-9]+' | tail -n1 || true)"

    if [ -n "$num" ]; then
        echo $((10#$num))
    else
        echo ""
    fi
}


# ------------------------------------------------------------
# Поиск элемента слота в sysfs по его номеру.
#
# Перебирает реальные записи /sys/class/enclosure/<enc>/*
# через glob-итерацию bash. Имена директорий могут содержать
# trailing-пробелы ("Bay Slot 03  ") — bash обрабатывает их
# корректно при итерации. Путь возвращается строго как получен
# из glob, без какой-либо обрезки пробелов.
#
# Аргументы:
#   $1 - адрес enclosure, например: 0:0:35:0
#   $2 - номер слота (целое число)
#
# Возвращает: полный путь к найденному элементу (с пробелами)
# ------------------------------------------------------------
find_slot_dir() {
    local enc="$1"
    local slot_num="$2"
    local enc_dir="/sys/class/enclosure/$enc"

    if [ ! -d "$enc_dir" ]; then
        echo "ОШИБКА: каталог enclosure не найден: $enc_dir" >&2
        return 1
    fi

    local d base n

    for d in "$enc_dir"/*; do
        path_exists_or_link "$d" || continue
        base="$(basename "$d")"

        # Пропускаем служебные записи sysfs
        case "$base" in
            device|power|subsystem|components|id|uevent)
                continue
                ;;
        esac

        n="$(extract_slot_number "$base")"

        if [ -n "$n" ] && [ "$n" -eq "$slot_num" ]; then
            # Возвращаем путь как есть через printf без newline,
            # чтобы $() не обрезал trailing-пробелы вместе с \n.
            # Trailing-пробелы — часть реального имени в sysfs.
            printf '%s' "$d"
            return 0
        fi
    done

    echo "ОШИБКА: не удалось найти элемент для слота $slot_num в $enc_dir" >&2
    echo "Доступные элементы:" >&2
    for d in "$enc_dir"/*; do
        path_exists_or_link "$d" || continue
        basename "$d" >&2
    done
    return 1
}


# ------------------------------------------------------------
# Проверка прав root
# ------------------------------------------------------------
if [ "$(id -u)" -ne 0 ]; then
    echo "ОШИБКА: скрипт должен запускаться от root"
    exit 1
fi


# ------------------------------------------------------------
# Проверка аргументов
# ------------------------------------------------------------
[ $# -eq 2 ] || usage

DISK_RAW="$1"
ACTION_RAW="$2"
DISK="${DISK_RAW#/dev/}"


# ------------------------------------------------------------
# Нормализация действия
# ------------------------------------------------------------
case "${ACTION_RAW,,}" in
    on|1)       ACTION="on"  ;;
    off|0|oof)  ACTION="off" ;;
    *)
        echo "ОШИБКА: неизвестное действие: $ACTION_RAW"
        usage
        ;;
esac


# ------------------------------------------------------------
# Проверка существования дискового устройства
# ------------------------------------------------------------
if [ ! -b "/dev/$DISK" ]; then
    echo "ОШИБКА: устройство /dev/$DISK не существует"
    exit 1
fi

if ! path_exists_or_link "/sys/block/$DISK/device"; then
    echo "ОШИБКА: отсутствует путь /sys/block/$DISK/device"
    exit 1
fi


# ------------------------------------------------------------
# Получение SCSI-адреса и target через sysfs
# ------------------------------------------------------------
SCSI_ADDR="$(basename "$(readlink -f "/sys/block/$DISK/device")")"

if [[ ! "$SCSI_ADDR" =~ ^[0-9]+:[0-9]+:[0-9]+:[0-9]+$ ]]; then
    echo "ОШИБКА: неожиданный SCSI-адрес: $SCSI_ADDR"
    exit 1
fi

# Используем _ для неиспользуемых полей (host, channel, lun)
IFS=':' read -r _ _ TARGET _ <<< "$SCSI_ADDR"


# ------------------------------------------------------------
# Определение enclosure и номера слота по SCSI target
# ------------------------------------------------------------
ENC=""
ENC_NAME=""
SLOT=""

if (( TARGET >= 0 && TARGET <= 23 )); then
    ENC="0:0:24:0"
    ENC_NAME="IN-WIN RS-424-07 (24 слота)"
    SLOT=$((TARGET + 1))
elif (( TARGET == 24 )); then
    echo "ОШИБКА: SCSI target 24 зарезервирован для enclosure-контроллера RS-424-07"
    echo "SCSI-адрес устройства: $SCSI_ADDR"
    echo "Это не дисковый слот — управление LED невозможно."
    exit 1
elif (( TARGET >= 25 && TARGET <= 36 )); then
    ENC="0:0:35:0"
    ENC_NAME="IN-WIN RS-212-07 (12 слотов)"
    SLOT=$((TARGET - 24))
else
    echo "ОШИБКА: SCSI target $TARGET не попадает ни в одну известную корзину"
    echo "SCSI-адрес устройства: $SCSI_ADDR"
    exit 1
fi


# ------------------------------------------------------------
# Поиск элемента слота.
#
# SLOT_DIR     — рабочий путь с оригинальными trailing-пробелами,
#                используется для обращения к файловой системе.
# SLOT_DIR_DISPLAY — путь без trailing-пробелов, только для вывода.
# ------------------------------------------------------------
if ! SLOT_DIR="$(find_slot_dir "$ENC" "$SLOT")"; then
    exit 1
fi
SLOT_DIR_DISPLAY="$(trim_trailing_spaces "$SLOT_DIR")"


# ------------------------------------------------------------
# Поиск файла управления LED.
#
# Приоритет: locate (синий, поиск диска) > fault (красный, ошибка)
# ------------------------------------------------------------
LED_FILE=""
LED_KIND=""

if path_exists_or_link "$SLOT_DIR/locate"; then
    LED_FILE="$SLOT_DIR/locate"
    LED_KIND="locate"
elif path_exists_or_link "$SLOT_DIR/fault"; then
    LED_FILE="$SLOT_DIR/fault"
    LED_KIND="fault"
else
    echo "ОШИБКА: в элементе слота нет доступного LED-файла"
    echo "Элемент слота: $SLOT_DIR_DISPLAY"
    echo
    echo "Содержимое элемента:"
    ls -la "$SLOT_DIR/" 2>/dev/null || true
    exit 1
fi

# Путь к LED-файлу для вывода — тоже без trailing-пробелов
LED_FILE_DISPLAY="${SLOT_DIR_DISPLAY}/${LED_KIND}"


# ------------------------------------------------------------
# Включение / выключение LED
# ------------------------------------------------------------
if [ "$ACTION" = "on" ]; then
    LED_VALUE=1
    ACTION_TEXT="ВКЛЮЧЕНА"
else
    LED_VALUE=0
    ACTION_TEXT="ВЫКЛЮЧЕНА"
fi

if ! echo "$LED_VALUE" > "$LED_FILE"; then
    echo "ОШИБКА: не удалось записать в LED-файл: $LED_FILE_DISPLAY"
    echo "Проверьте состояние enclosure и поддержку LED."
    exit 1
fi


# ------------------------------------------------------------
# Проверка фактического состояния после записи.
#
# sysfs-файлы locate/fault возвращают 0 (выключен) или 1 (включён).
# ------------------------------------------------------------
CURRENT_STATE_RAW="$(cat "$LED_FILE" 2>/dev/null || echo '?')"

case "$CURRENT_STATE_RAW" in
    0) CURRENT_STATE="выключен" ;;
    1) CURRENT_STATE="включён"  ;;
    *) CURRENT_STATE="неизвестно ($CURRENT_STATE_RAW)" ;;
esac


# ------------------------------------------------------------
# Итог
# ------------------------------------------------------------
cat <<EOF
========================================
 Результат работы скрипта
========================================
Диск                 : /dev/$DISK
SCSI-адрес           : $SCSI_ADDR
SCSI target          : $TARGET

Корзина              : $ENC_NAME
Адрес enclosure      : $ENC
Номер слота          : $SLOT
Элемент слота        : $SLOT_DIR_DISPLAY

Тип индикатора       : $LED_KIND
Выполненное действие : $ACTION_TEXT
Текущее состояние    : $CURRENT_STATE

Путь к LED-файлу     : $LED_FILE_DISPLAY
========================================
EOF
